-- Taskly example schema + seed data (PostgreSQL).
-- Relationships are real foreign keys here — Graphoria introspects them into the
-- API graph automatically. The exception is task_tags, which carries no FKs; its
-- relationships (from tasks, and to tags) stay declared in graphoria.ts to
-- demonstrate the config-declared relationships feature.

CREATE TABLE IF NOT EXISTS organizations (
  id         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name       text NOT NULL,
  slug       text NOT NULL,
  plan       text NOT NULL DEFAULT 'free',      -- free | pro | enterprise
  created_at timestamptz NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS projects (
  id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  org_id      integer NOT NULL REFERENCES organizations(id),
  owner       text    NOT NULL,                 -- auth.user.username
  name        text    NOT NULL,
  description text,
  visibility  text    NOT NULL DEFAULT 'org',   -- private | org | public
  status      text    NOT NULL DEFAULT 'active',
  created_at  timestamptz NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS tasks (
  id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  org_id      integer NOT NULL REFERENCES organizations(id),  -- denormalized → one-liner RBAC row filters
  project_id  integer NOT NULL REFERENCES projects(id),
  assignee    text,                             -- auth.user.username
  created_by  text    NOT NULL,
  title       text    NOT NULL,
  description text,
  status      text    NOT NULL DEFAULT 'todo',  -- todo | in_progress | done
  priority    integer NOT NULL DEFAULT 3,       -- 1..5
  due_date    timestamptz,
  created_at  timestamptz NOT NULL DEFAULT NOW(),
  updated_at  timestamptz NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS comments (
  id         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  org_id     integer NOT NULL REFERENCES organizations(id),
  task_id    integer NOT NULL REFERENCES tasks(id),
  author     text    NOT NULL,
  body       text    NOT NULL,
  created_at timestamptz NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS tags (
  id     integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  org_id integer NOT NULL REFERENCES organizations(id),
  name   text    NOT NULL,
  color  text    NOT NULL DEFAULT '#888888'
);

-- Intentionally FK-free: task_tags' relationships (from tasks, to tags) are
-- declared in graphoria.ts to demonstrate config-declared relationships.
CREATE TABLE IF NOT EXISTS task_tags (
  task_id integer NOT NULL,
  tag_id  integer NOT NULL,
  PRIMARY KEY (task_id, tag_id)
);

CREATE TABLE IF NOT EXISTS audit_log (
  id         integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  org_id     integer NOT NULL REFERENCES organizations(id),
  actor      text    NOT NULL,
  action     text    NOT NULL,
  entity     text    NOT NULL,
  created_at timestamptz NOT NULL DEFAULT NOW()
);

-- Function backing the `age_days` virtual column.
CREATE OR REPLACE FUNCTION task_age_days(ts timestamptz)
RETURNS integer LANGUAGE sql STABLE AS $$
  SELECT GREATEST(0, EXTRACT(DAY FROM (NOW() - ts)))::integer;
$$;

-- Stored procedure exposed via the `storedProcedures` permission key.
CREATE OR REPLACE FUNCTION project_stats(p_project_id integer)
RETURNS TABLE(open_count integer, in_progress_count integer, done_count integer)
LANGUAGE sql STABLE AS $$
  SELECT
    COUNT(*) FILTER (WHERE status = 'todo')::integer,
    COUNT(*) FILTER (WHERE status = 'in_progress')::integer,
    COUNT(*) FILTER (WHERE status = 'done')::integer
  FROM tasks WHERE project_id = p_project_id;
$$;

